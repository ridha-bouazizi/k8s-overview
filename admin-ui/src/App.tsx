import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { UserList } from "./user/UserList";
import { UserCreate } from "./user/UserCreate";
import { UserEdit } from "./user/UserEdit";
import { UserShow } from "./user/UserShow";
import { ServiceEndpointList } from "./serviceEndpoint/ServiceEndpointList";
import { ServiceEndpointCreate } from "./serviceEndpoint/ServiceEndpointCreate";
import { ServiceEndpointEdit } from "./serviceEndpoint/ServiceEndpointEdit";
import { ServiceEndpointShow } from "./serviceEndpoint/ServiceEndpointShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"K8S Overview"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="User"
          list={UserList}
          edit={UserEdit}
          create={UserCreate}
          show={UserShow}
        />
        <Resource
          name="ServiceEndpoint"
          list={ServiceEndpointList}
          edit={ServiceEndpointEdit}
          create={ServiceEndpointCreate}
          show={ServiceEndpointShow}
        />
      </Admin>
    </div>
  );
};

export default App;
